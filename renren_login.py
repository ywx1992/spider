ssion = requests.session()

headers = {
    "User-Agent": "Mozilla/5.0 (Windows NT 6.1; WOW64) AppleWebKit/534.50 (KHTML, like Gecko) Version/5.1 Safari/534.50"}

data = {"email": "15692092379", "password": "614131504"}

# 发送附带用户名和密码的请求，获取登陆后的cookie值，并保存在ssion中
ssion.post("http://www.renren.com/PLogin.do", data=data, headers=headers)

# ssion中包含用户登录后的Cookie值，可以直接访问登陆后才能访问的页面
response = ssion.get("http://www.renren.com/320031429/profile", headers=headers)

print(response.content.decode())

